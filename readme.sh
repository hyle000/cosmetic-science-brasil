Projetado como sistema de templates editáveis no Pinegrow Web Editor, seguindo padrões modernos de desenvolvimento web com foco em usabilidade editorial e experiência do leitor acadêmico.

---

## 📋 Sobre o Projeto

Portal digital da **Cosmetic Science Brasil**, uma revista científica especializada em:

- 🔬 Pesquisas originais em ciência cosmética
- 🧴 Formulação e desenvolvimento de produtos
- 🌿 Biodiversidade brasileira aplicada à cosmética
- 📊 Regulamentação e tendências do setor

### ✨ Funcionalidades

| Funcionalidade | Descrição |
|----------------|-----------|
| 🔍 Busca Avançada | Pesquisa por termo, categoria, período e tipo de conteúdo |
| 📄 Artigos Científicos | Visualização com DOI, autores, métricas e referências |
| 🔒 Área do Assinante | Dashboard personalizado com histórico e favoritos |
| 📅 Calendário de Eventos | Visualização mensal com filtros por tipo e local |
| 📰 Mídia Kit | Página completa para anunciantes com formatos e cases |
| 👤 Autenticação | Login, cadastro e recuperação de senha |
| 📱 Responsivo | Layout adaptável para mobile, tablet e desktop |
| ♿ Acessibilidade | Roles ARIA, skip links e navegação por teclado |
| 🔗 SEO | Schema.org para artigos, meta tags e Open Graph |

